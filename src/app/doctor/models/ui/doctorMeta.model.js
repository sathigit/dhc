function DoctorMetadataUI(doctorMetadataServer) {
    if( doctorMetadataServer ) {
        this.aadharNumber = doctorMetadataServer.aadharNumber
        this.aboutDoctor = doctorMetadataServer.aboutDoctor;
        this.active = doctorMetadataServer.active;
        this.address = doctorMetadataServer.address;
        this.bloodGroup = doctorMetadataServer.bloodGroup;
        this.city = doctorMetadataServer.city;
        this.country = doctorMetadataServer.country;
        this.dob = doctorMetadataServer.dob;
        this.donateOrgan = doctorMetadataServer.donateOrgan;
        this.feedbackCount = doctorMetadataServer.feedbackCount;
        this.firstName = doctorMetadataServer.firstName;
        this.gender = doctorMetadataServer.gender;
        this.ima = doctorMetadataServer.ima;
        this.imagePath = doctorMetadataServer.imagePath;
        this.isActive = doctorMetadataServer.isActive;
        this.lastName = doctorMetadataServer.lastName;
        this.latitude = doctorMetadataServer.latitude;
        this.longitude = doctorMetadataServer.longitude;
        this.middleName = doctorMetadataServer.middleName;
        this.nationality = doctorMetadataServer.nationality;
        this.phoneNumber = doctorMetadataServer.phoneNumber;
        this.rating = doctorMetadataServer.rating;
        this.state = doctorMetadataServer.state;
        this.taluk = doctorMetadataServer.taluk;
        this.userId = doctorMetadataServer.userId;
        this.village = doctorMetadataServer.village;
        this.zipCode = doctorMetadataServer.zipCode;        
    } else {
        this.aadharNumber = '';
        this.aboutDoctor = '';
        this.active = '';
        this.address = '';
        this.bloodGroup = '';
        this.city = '';
        this.country = '';
        this.dob = '';
        this.donateOrgan = '';
        this.feedbackCount = '';
        this.firstName = '';
        this.gender = '';
        this.ima = '';
        this.imagePath = '';
        this.isActive = '';
        this.lastName = '';
        this.latitude = '';
        this.longitude = '';
        this.middleName = '';
        this.nationality = '';
        this.phoneNumber = '';
        this.rating = '';
        this.state = '';
        this.taluk = '';
        this.userId = '';
        this.village = '';
        this.zipCode = '';
    }
}