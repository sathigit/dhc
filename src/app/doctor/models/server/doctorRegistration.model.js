function DoctorRegistrationServer(doctorUI) {
    this.name = doctorUI.name ;
    this.userName = doctorUI.userName;
    this.userType = doctorUI.userType;
    this.password = doctorUI.password;
    this.loginType = "DHC";
    this.email = doctorUI.email;
    this.mobile = doctorUI.mobile;
    this.orgId = 1; 
}
