function UserMetadataServer(userMetadataUI) {
    this.userId = userMetadataUI.userId;
    this.name = '';
    this.userName = userMetadataUI.userName;
    this.userType = userMetadataUI.userType;
    this.loginType = userMetadataUI.loginType;
    this.email = userMetadataUI.email;
    this.mobile = userMetadataUI.mobile;
}